// =======================
// ECHO SYSTEM
// =======================
function showEchoMessage(customPool = null) {
  const el = document.getElementById("echoMessage");

  const pool = customPool || echoMessages;
  const text = pool[Math.floor(Math.random() * pool.length)];

  el.textContent = "";
  el.style.opacity = 1;

  let i = 0;

  function type() {
    if (i < text.length) {
      el.textContent += text.charAt(i);
      i++;
      setTimeout(type, 35);
    } else {
      setTimeout(() => {
        el.style.opacity = 0;
      }, 2000);
    }
  }

  type();
}